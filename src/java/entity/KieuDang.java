package entity;

/**
 *
 * @author Admin
 */
public class KieuDang {
    private int id;
    private String name;
    private int quantity;

    public KieuDang() {
    }

    public KieuDang(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
    
}
